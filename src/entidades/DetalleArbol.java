	private String nombreComunArbol;
	private String nombreCientificoArbol;
	private String descripcionArbol;
	private String imgArbol;
	private String nombreFamilia;
	private String descripcionFamilia;
	private String nombreGenero;
	private String descripcionGenero;
	private String nombreComunFlor;
	private String nombreCientificoFlor;
	private String descripcionFlor;
	private String temporadaFloracion;
	private String nombreDistribucion;
