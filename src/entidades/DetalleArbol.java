	public void setDescripcionFlor(String descripcionFlor) {
		this.descripcionFlor = descripcionFlor;
	}

	public String getTemporadaFloracion() {
		return temporadaFloracion;
	}

	public void setTemporadaFloracion(String temporadaFloracion) {
		this.temporadaFloracion = temporadaFloracion;
	}
	
	

	public String getNombreDistribucion() {
		return nombreDistribucion;
	}



	public void setNombreDistribucion(String nombreDistribucion) {
		this.nombreDistribucion = nombreDistribucion;
	}



	public String getDescripcionDistribucion() {
		return descripcionDistribucion;
	}



	public void setDescripcionDistribucion(String descripcionDistribucion) {
		this.descripcionDistribucion = descripcionDistribucion;
	}



	public String getNombreRegion() {
		return nombreRegion;
	}

